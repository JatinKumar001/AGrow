const stars0 = document.querySelectorAll('.s0');

stars0.forEach((s0, i)=>{
    s0.onclick = function(){
        let currlevel0 = i + 1;

        stars0.forEach((s0, j)=>{
            if(currlevel0 >= j+1){
                s0.innerHTML = '&#9733';
            }
            else{
                s0.innerHTML = '&#9734';
            }
        })
    }
})

